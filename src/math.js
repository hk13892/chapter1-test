//add
function add(a,b){
    return a+b;
}
console.log(add(5,10));

//sub
function sub(a,b){
    return a-b;
}
console.log(sub(5,10));

//multiply
function multiply(a,b) {
    return a*b;
}
console.log(multiply(5,5));

//divide
function divide(a,b){
    return a/b;
}
console.log(divide(10,10));

//remainder
function rem(a,b){
    return a%b;
}
console.log(rem(20,3));